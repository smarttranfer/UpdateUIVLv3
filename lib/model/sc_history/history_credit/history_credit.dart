class history_credit{
  late int ID_log;
  late double credit;
  late String user_id;
  late String customer_id;
  late String create_date;

  int get ID_logs{
    return ID_log ;
  }

  void set ID_logs(int ID_log){
    ID_log = ID_log;
  }

  double get credits{
    return credit ;
  }

  void set credits(double credit){
    credit = credit;
  }

  String get user_ids{
    return user_id ;
  }

  void set user_ids(String user_id){
    user_id = user_id;
  }

  String get customer_ids{
    return customer_id ;
  }

  void set customer_ids(String customer_id){
    customer_id = customer_id;
  }

  String get create_dates{
    return create_date ;
  }

  void set create_dates(String create_date){
    create_date = create_date;
  }

}